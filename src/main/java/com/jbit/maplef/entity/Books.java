package com.jbit.maplef.entity;


public class Books {

  private Integer bookId;
  private String bookIdx;
  private String bookName;
  private String bookAuthor;
  private Integer bookPub;
  private Integer bookSell;
  private double bookMoney;
  private Integer bookIntegrity;
  private Integer bookrackId;
  private Integer bookSort;
  private String bookDesc;
  private Integer bookState;


  public Integer getBookId() {
    return bookId;
  }

  public void setBookId(Integer bookId) {
    this.bookId = bookId;
  }


  public String getBookIdx() {
    return bookIdx;
  }

  public void setBookIdx(String bookIdx) {
    this.bookIdx = bookIdx;
  }


  public String getBookName() {
    return bookName;
  }

  public void setBookName(String bookName) {
    this.bookName = bookName;
  }


  public String getBookAuthor() {
    return bookAuthor;
  }

  public void setBookAuthor(String bookAuthor) {
    this.bookAuthor = bookAuthor;
  }


  public Integer getBookPub() {
    return bookPub;
  }

  public void setBookPub(Integer bookPub) {
    this.bookPub = bookPub;
  }


  public Integer getBookSell() {
    return bookSell;
  }

  public void setBookSell(Integer bookSell) {
    this.bookSell = bookSell;
  }


  public double getBookMoney() {
    return bookMoney;
  }

  public void setBookMoney(double bookMoney) {
    this.bookMoney = bookMoney;
  }


  public Integer getBookIntegrity() {
    return bookIntegrity;
  }

  public void setBookIntegrity(Integer bookIntegrity) {
    this.bookIntegrity = bookIntegrity;
  }


  public Integer getBookrackId() {
    return bookrackId;
  }

  public void setBookrackId(Integer bookrackId) {
    this.bookrackId = bookrackId;
  }


  public Integer getBookSort() {
    return bookSort;
  }

  public void setBookSort(Integer bookSort) {
    this.bookSort = bookSort;
  }


  public String getBookDesc() {
    return bookDesc;
  }

  public void setBookDesc(String bookDesc) {
    this.bookDesc = bookDesc;
  }


  public Integer getBookState() {
    return bookState;
  }

  public void setBookState(Integer bookState) {
    this.bookState = bookState;
  }

}
