package com.jbit.maplef.entity;


public class Bookrack {

  private Integer bookrackId;
  private String bookrackCode;
  private String bookrackLocation;
  private Integer bookSort;


  public Integer getBookrackId() {
    return bookrackId;
  }

  public void setBookrackId(Integer bookrackId) {
    this.bookrackId = bookrackId;
  }


  public String getBookrackCode() {
    return bookrackCode;
  }

  public void setBookrackCode(String bookrackCode) {
    this.bookrackCode = bookrackCode;
  }


  public String getBookrackLocation() {
    return bookrackLocation;
  }

  public void setBookrackLocation(String bookrackLocation) {
    this.bookrackLocation = bookrackLocation;
  }


  public Integer getBookSort() {
    return bookSort;
  }

  public void setBookSort(Integer bookSort) {
    this.bookSort = bookSort;
  }

}
